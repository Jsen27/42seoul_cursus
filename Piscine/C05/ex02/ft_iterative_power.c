int	ft_iterative_power(int nb, int power)
{
	int	ans;

	ans = 1;
	if (power < 0)
		return (0);
	else if (power == 1)
		return (nb);
	while (power > 0)
	{
		ans *= nb;
		power--;
	}
	return (ans);
}
